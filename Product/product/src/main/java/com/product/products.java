package com.product;

public class products {
	private int pid;
	private String pname;
	private int price;
	public int getpid() {
		return pid;
	}
	public void setpid(int pid) {
		this.pid = pid;
	}
	
	public String getpname() {
		return pname;
	}
	public void setpname(String pname) {
		this.pname = pname;
	}
	public int getprice() {
		return price;
	}
	public void setprice(int price) {
		this.price = price;	
	}

}
